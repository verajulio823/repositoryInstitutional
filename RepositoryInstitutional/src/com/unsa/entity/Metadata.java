package com.unsa.entity;

public class Metadata {
	
	private String author;
	private String creator;
	private String issued;
	private String abstract_doc;
	private String other;
	private String description;
	private String source;
	private String title;
	private String publisher;
	private String type;
	private String language_iso;
	private String subject;	
	
	public  Metadata(){
		
	}
	
	public String getAuthor() {
		return author;
	}
	public void setAuthor(String author) {
		this.author = author;
	}
	public String getCreator() {
		return creator;
	}
	public void setCreator(String creator) {
		this.creator = creator;
	}
	public String getIssued() {
		return issued;
	}
	public void setIssued(String issued) {
		this.issued = issued;
	}
	public String getAbstract_doc() {
		return abstract_doc;
	}
	public void setAbstract_doc(String abstract_doc) {
		this.abstract_doc = abstract_doc;
	}
	public String getOther() {
		return other;
	}
	public void setOther(String other) {
		this.other = other;
	}
	public String getDescription() {
		return description;
	}
	public void setDescription(String description) {
		this.description = description;
	}
	public String getSource() {
		return source;
	}
	public void setSource(String source) {
		this.source = source;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getPublisher() {
		return publisher;
	}
	public void setPublisher(String publisher) {
		this.publisher = publisher;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public String getLanguage_iso() {
		return language_iso;
	}
	public void setLanguage_iso(String language_iso) {
		this.language_iso = language_iso;
	}
	public String getSubject() {
		return subject;
	}
	public void setSubject(String subject) {
		this.subject = subject;
	}
	
	
	

}
